package day13_conditional_statements;

public class NiceDayOrNot {
    public static void main(String[] args) {
        int temp = 71;
        if (temp >= 70) {
            System.out.println("It is a nice day out");
            System.out.println("Lets do some Java then!!!");
        } else {
            System.out.println("It is not a nice day out");
            System.out.println("Stay home!!!");
        }

    }
}
